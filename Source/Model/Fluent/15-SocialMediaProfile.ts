///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
'use strict'; /// Strict Syntax //////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Sequelize Dependencies ///////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import {
	BelongsTo,
	Column,
	DataType,
	IsUrl,
	Model,
	Table
} from 'sequelize-typescript';

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
import User from './05-User'; /// User Model /////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// SocialMediaProfile Table Model Definition ////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

@Table({
	indexes: [
		{fields: ['is_active']},
		{fields: ['type']},
		{fields: ['user_id']}
	],
	tableName: 'social_media_profile'
})
export default class SocialMediaProfile extends Model<SocialMediaProfile> {

	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/// Columns //////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	@Column({
		allowNull: false,
		defaultValue: DataType.UUIDV4,
		field: 'id',
		primaryKey: true,
		type: DataType.UUID
	})
	id!: string;

	@Column({
		allowNull: false,
		defaultValue: true,
		field: 'is_active',
		type: DataType.BOOLEAN,
	})
	isActive!: boolean;

	@Column({
		allowNull: true,
		field: 'token',
		type: DataType.TEXT
	})
	token!: string;


	@Column({
		allowNull: false,
		field: 'type',
		type: DataType.ENUM(
			'facebook',
			'linkedin',
			'googleplus',
			'github',
			'gitlab'
		)
	})
	type!: string;

	@Column({
		allowNull: true,
		field: 'url',
		type: DataType.TEXT
	})
	@IsUrl
	url!: string;

	@Column({
		allowNull: false,
		field: 'user_id',
		type: DataType.UUID
	})
	userId!: string;

	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/// Associations /////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	@BelongsTo(() => User)
	user!: User;
};
